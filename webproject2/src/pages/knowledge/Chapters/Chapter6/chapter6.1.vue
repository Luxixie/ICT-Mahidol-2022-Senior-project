            <p style="margin-left: 3%; ">Beta is a measurement of a stock’s volatility compared to the overall markets. The markets have a beta of 1. If a stock has a beta of 1.5, it means that for every 1-point move in the market, the stock moves 1.5 points. That means the stock’s more volatile than the market.</p>
