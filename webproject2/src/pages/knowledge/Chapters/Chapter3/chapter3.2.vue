            <h2 style="margin-left: 3%;  margin-top:2%">Concept</h2>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%">   Just as stock investors buy equity in a company, bond investors buy debt. Companies and governments issue bonds, collecting cash—the bond’s face value or par value—up front from investors. The issuers agree to make interest payments, usually semiannually, for a set time before paying the money back.</h4>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%"> In most cases, the payment remains the same for the life of the bond. For this reason, bonds and other securities that make regular payments are classified as fixed-income. In later chapters, during the discussion about how to craft a portfolio, you’ll read about seeking a balance between equities and fixed-income (or stocks and bonds).</h4>
            <h4  style="margin-left: 3%; margin-right: 3%;  margin-top:1%">Estimates vary regarding the size of the U.S. bond market — which contains debt issued by corporations, the federal government, and municipalities—but most peg the bond market at roughly twice the size of the stock market.</h4>
            
            <h2 style="margin-left: 3%;  margin-top:2%">Classifications</h2>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%"> Bonds come in many varieties. Here are a few of the most common:</h4>
            <p style="margin-left: 5%; margin-right: 3%;  margin-top:1%">·  <em style="font-weight: bold;">Corporate bonds. </em> Debt issued by corporations.</p>
            <p style="margin-left: 5%; margin-right: 3%;  margin-top:1%">·  <em style="font-weight: bold;">Treasury bonds. </em> Debt issued by the U.S. Treasury and backed by the full faith and credit of the federal government. Investors both in the United States and overseas generally consider Treasury bonds free of default risk.</p>
            <p style="margin-left: 5%; margin-right: 3%;  margin-top:1%">·  <em style="font-weight: bold;">Agency bonds. </em> Debt issued by agencies connected to the federal government. While such bonds don’t technically have the backing of the U.S. Treasury, most investors assume they have extremely low risk of default.</p>
            <p style="margin-left: 5%; margin-right: 3%;  margin-top:1%">·  <em style="font-weight: bold;">Municipal bonds. </em>Debt issued by states, municipalities, or agencies connected to states or municipalities, such as water systems. In most cases, the interest payments from these bonds are not subject to federal income taxes.</p>
            <p style="margin-left: 5%; margin-right: 3%;  margin-top:1%">·  <em style="font-weight: bold;">High-yield bonds.  </em> Also known as junk bonds, any securities with a speculative-grade rating fall into this group. Remember the concept: “High risk, high return. Low risk, low return.”</p>
            <p style="margin-left: 5%; margin-right: 3%;  margin-top:1%">·  <em style="font-weight: bold;">Convertible bonds. </em> Companies can issue bonds that, under certain conditions, convert into stock.</p>
            <p style="margin-left: 5%; margin-right: 3%;  margin-top:1%">·  <em style="font-weight: bold;">Variable-rate bonds.</em> In some cases, bond issuers make interest payments that fluctuate based on changes in a benchmark interest rate.</p>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%"> When you buy a bond, you make a bet on the creditworthiness of the issuer. If a bond issuer can no longer cover the payments, it may default on the bond, leaving investors in the lurch. The risk of default is known as credit risk.</h4>

            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%"> Credit-rating agencies—Standard & Poor’s, Moody’s, and Fitch Ratings— assess the creditworthiness of companies and governments that issue bonds and assign them ratings. Those ratings reflect the agencies’ opinions
                on the likelihood that the issuer will default. For example, S&P’s ratings range from AAA to D, with everything BBB-and higher classified as investment-grade. The agency considers every bond with a rating of BB+ or lower speculative-grade.</h4>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%"> 
                Standard & Poor’s issues AAA ratings only for the financially strongest companies. Fewer than 15 countries and only four companies earn AAA ratings from S&P.</h4>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%"> Many stocks pay dividends, but over the long haul stock investors generate most of their return from price appreciation—the change in the price of the stock. Bonds don’t work that way. Their prices will change, but most investors choose bonds because of their interest payments. Because issuers pay back the bonds at the end of their term, also known as maturity, bond prices tend to revert to the face value of the bond as the maturity date nears. Assuming an investor purchases a bond, collects the interest payments, and then recoups the face value of the bond at maturity, the changes to a bond’s price along the way don’t mean anything.</h4>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%">Unlike stock prices, which tend to fluctuate based on economic and company news, bonds respond mostly to interest rates or changes in a company’s perceived credit risk. If a company reports a strong quarter with higher-than-expected profits, the stock price might rise while the bonds remain steady. But if the company announces it has borrowed a lot of money and credit analysts begin to doubt its ability to satisfy its obligations, the company’s bonds could lose value.</h4>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%"> Bond prices react to changes in interest rates because their fixed payments look more attractive when interest rates fall and less attractive when rates rise. For example, suppose Acme Widget issues $1 billion in 10- year bonds at 5% during a period when the 10-year Treasury yields 3.5%. Since investors can collect a 3.5% yield without risking default, Acme’s 1.5% higher interest rate serves as compensation to investors for absorbing the credit risk. If Treasury yields rise to 4.5%, the appeal of Acme’s bonds declines; investors demand more than 0.5% for taking on the credit risk of the corporate bond. As a result, Acme’s bonds likely dip in price, possibly to a point where they yield roughly 1.5% more than Treasury bonds. The reverse would happen if Treasury yields declined—Acme could lower its rates accordingly and still be seen as a risk worth taking.</h4>
            <h4 style="margin-left: 3%; margin-right: 3%;  margin-top:1%"> Long-term bonds tend to react to changes in interest rates more strongly than do short-term bonds. Remember, bond prices tend to revert to par
                value as they near maturity. A bond that matures in three months won’t see its value change much even if interest rates migrate, because in three months the issuer will redeem it for its face value. But a bond that matures in 20 years—and has 40 semiannual coupon payments to make before maturity—will react more violently to changes in interest rates, especially as investors reprice the bond to take into account its yield relative to the market.</h4>

